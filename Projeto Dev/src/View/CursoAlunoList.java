    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        String result = "" + tabela.getValueAt(tabela.getSelectedRow(), 1).toString();
        cbAluno.addItem(result);
        cbAluno.setSelectedItem(result);
        txtClasse.setText(tabela.getValueAt(tabela.getSelectedRow(), 3).toString());
        txtCodigo.setText(tabela.getValueAt(tabela.getSelectedRow(), 0).toString());
        String result2 = "" + tabela.getValueAt(tabela.getSelectedRow(), 2).toString();
        cbCurso.addItem(result2);
        cbCurso.setSelectedItem(result2);
    }//GEN-LAST:event_tabelaMouseClicked

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int selecionar = tabela.getSelectedRow() ;
        int idselect = 0;
        if(selecionar <= -1){
            JOptionPane.showMessageDialog(this, "Nenhum Registro Selecionado!");
            return;
        }else{
            idselect = (Integer.parseInt(tabela.getValueAt(selecionar, 0).toString()));
            int linha = cursoAlunoController.deletarCursoAluno(idselect);
            if(linha > 0 ){
                JOptionPane.showMessageDialog(this, "Excluido com sucesso!");
            }
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        int selecionar = tabela.getSelectedRow() ;
        int idselect = 0;
        if(selecionar <= -1){
            JOptionPane.showMessageDialog(this, "Nenhum Registro Selecionado!");
            return;
        }
        else{
                
                alunoModel = (Aluno) cbAluno.getSelectedItem();
                cursoModel = (Curso) cbCurso.getSelectedItem();
                cursoAlunoModel.setClasse(txtClasse.getText());
                cursoAlunoModel.setAluno(alunoModel);
                cursoAlunoModel.setCurso(cursoModel);
                cursoAlunoModel.setCodigo(Integer.parseInt(txtCodigo.getText()));
                cursoAlunoController.atualizarCursoAluno(cursoAlunoModel);
                JOptionPane.showMessageDialog(this, "Alterado com sucesso");
                txtClasse.requestFocus();
               }
    }//GEN-LAST:event_btEditarActionPerformed

