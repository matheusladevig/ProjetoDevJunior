    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if(cbAluno.getSelectedItem() == null){
            JOptionPane.showMessageDialog(this, "Informe o Nome!");
            cbAluno.requestFocus();
         }if(cbCurso.getSelectedItem() == null){
            JOptionPane.showMessageDialog(this, "Informe o Nome!");
            cbCurso.requestFocus();
         }if(txtClasse.getText() == null || txtClasse.getText().trim().equals("") ){
            JOptionPane.showMessageDialog(this, "Informe o Nome!");
            txtClasse.requestFocus();
         }
         else {
             alunoModel = (Aluno) cbAluno.getSelectedItem();
             cursoModel = (Curso) cbCurso.getSelectedItem();
             cursoAlunoModel.setClasse(txtClasse.getText());
             cursoAlunoModel.setAluno(alunoModel);
             cursoAlunoModel.setCurso(cursoModel);
             cursoAlunoController.salvarCursoAluno(cursoAlunoModel);
             JOptionPane.showMessageDialog(this, "Salvo com sucesso");
        } 
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisaActionPerformed
        String descricao;
        if(txtPesquisa.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Nada informado");
            descricao = null;
        }else{
            descricao = txtPesquisa.getText();
        }
        
        
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setNumRows(0);
        
        for(CursoAluno v : cursoAlunoController.listaCursoAluno(descricao)){
            modelo.addRow(new Object[]{
                v.getCodigo(),
                v.getCodigo_aluno(),
                v.getCodigo_curso(),
                v.getClasse()
                
                 
            });
        }
    }//GEN-LAST:event_btPesquisaActionPerformed

