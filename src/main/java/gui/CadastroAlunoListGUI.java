    private void salvar() throws HeadlessException {
        Integer codigo;
        if (TextNome.getText() == null || TextNome.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Informe o Nome!");
            TextNome.requestFocus();
        } else {
            if(Id.getText().isEmpty()){
            codigo = null;
            alunoModel.setId(codigo);
            situacaoCadastroModel = (SituacaoCadastro) cbCadastro.getSelectedItem();
            alunoModel.setNome(TextNome.getText());
            alunoModel.setSituacaoCadastro(situacaoCadastroModel);
            alunoController.salvarAluno(alunoModel);
            JOptionPane.showMessageDialog(this, "Salvo com sucesso");
            TextNome.requestFocus();
            }else{
            situacaoCadastroModel = (SituacaoCadastro) cbCadastro.getSelectedItem();
            alunoModel.setId(Integer.parseInt(Id.getText()));
            alunoModel.setNome(TextNome.getText());
            alunoModel.setSituacaoCadastro(situacaoCadastroModel);
            alunoController.salvarAluno(alunoModel);
            JOptionPane.showMessageDialog(this, "Salvo com sucesso");
            TextNome.requestFocus();
            }
        }
    }

